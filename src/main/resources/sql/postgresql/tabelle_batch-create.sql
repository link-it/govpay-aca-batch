CREATE INDEX idx_batch_step_execution_1 ON BATCH_STEP_EXECUTION(VERSION);
CREATE INDEX idx_batch_step_execution_2 ON BATCH_STEP_EXECUTION(STEP_NAME,JOB_EXECUTION_ID);
